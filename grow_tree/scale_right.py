def scale_node_coordinates(input_text, scale_factors):
    scaled_lines = []
    lines = input_text.splitlines()
    node_idx = -1

    for line in lines:
        if line.strip().startswith("Node:"):
            node_idx = 0  # reset index for each new node
            scaled_lines.append(line)
        elif node_idx >= 0 and node_idx < 3:
            values = line.strip().split()
            scale_key = ['x', 'y', 'z'][node_idx]
            scaled_values = [f"{float(val) * scale_factors[scale_key]:.6f}" for val in values]
            scaled_line = "  " + " ".join(scaled_values)
            scaled_lines.append(scaled_line)
            node_idx += 1
        else:
            scaled_lines.append(line)

    return "\n".join(scaled_lines)

